package POJO;

public class User {
    public String name;
    public String lastname;
    public String middlename;
    public String age;
    public String language;

    public User(String name, String lastname, String middlename, String age, String language){
        this.name = name;
        this.lastname = lastname;
        this.middlename = middlename;
        this.age = age;
        this.language = language;
    }
}
